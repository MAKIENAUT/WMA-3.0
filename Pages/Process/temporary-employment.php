            <div class="steps-content">
               <div class="steps-header">
                  <p>How to Apply?</p>
                  <h1>FOLLOW THESE STEPS!</h1>
                  <p>The H-2A visa is a temporary work visa for foreign agricultural workers who will perform seasonal or temporary agricultural work in the United States. Here is an overview of the general process of obtaining an H-2A visa:</p>
               </div>
               <div class="steps-description">
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>01</h1>
                        <h1>EMPLOYER RECRUITMENT</h1>
                     </div>
                     <p>The U.S. employer must first demonstrate that there are not enough U.S. workers who are able, willing, qualified, and available to do the temporary work, and that hiring foreign workers will not adversely affect the wages and working conditions of U.S. workers similarly employed.</p>
                  </div>
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>02</h1>
                        <h1>FILE A LABOR CERTIFICATION APPLICATION</h1>
                     </div>
                     <p>The U.S. employer must file a labor certification application with the U.S. Department of Labor (DOL) to demonstrate that they have met the recruitment requirements and that the job offer meets the minimum requirements set by the DOL.</p>
                  </div>
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>03</h1>
                        <h1>SUBMIT FORM I-129</h1>
                     </div>
                     <p>The U.S. employer must file a Form I-129, Petition for Nonimmigrant Worker, with U.S. Citizenship and Immigration Services (USCIS), along with supporting documentation and the approved labor certification.</p>
                  </div>
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>04</h1>
                        <h1>CONSULAR PROCESSING</h1>
                     </div>
                     <p>Once the petition is approved, the foreign worker must apply for a visa at a U.S. consulate or embassy abroad.</p>
                  </div>
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>05</h1>
                        <h1>ADMISSIBILITY</h1>
                     </div>
                     <p>The foreign worker must demonstrate that they are admissible to the United States and not subject to any grounds of inadmissibility such as criminal history, health concerns, or prior immigration violations.</p>
                  </div>
                  <div class="steps-card">
                     <div class="steps-card-header">
                        <h1>06</h1>
                        <h1>ARRIVAL IN THE UNITED STATES</h1>
                     </div>
                     <p>If the visa is approved, the foreign worker can enter the United States and begin work for the U.S. employer.</p>
                  </div>
               </div>
               <div class="steps-conclusion">
                     <p>It is important to note that the H-2A visa process can be complex, and it is highly recommended that employers seek the assistance of an experienced immigration attorney to ensure compliance with all legal requirements.</p>
                     
               </div>
